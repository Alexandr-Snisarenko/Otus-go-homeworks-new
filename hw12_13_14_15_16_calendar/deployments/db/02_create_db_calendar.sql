-- Database: calendar

-- DROP DATABASE IF EXISTS calendar;

CREATE DATABASE calendar
    WITH
    OWNER = postgres
    ENCODING = 'UTF8'
    LC_COLLATE = 'en_US.utf8'
    LC_CTYPE = 'en_US.utf8'
    LOCALE_PROVIDER = 'libc'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1
    IS_TEMPLATE = False;

GRANT TEMPORARY, CONNECT ON DATABASE calendar TO PUBLIC;

GRANT ALL ON DATABASE calendar TO postgres;